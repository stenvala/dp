function [X W] = getGaussNodesStandardLine(degree)
  % Get Gauss nodes and weights for a standard line (from 0 to 1)in barycentric
  % coordinates. Reference: http://pomax.github.io/bezierinfo/legendre-gauss.html
  %
  % output:
  %   - X includes the barycentric coordinates
  %   - W includes the weights
  %
  % parameters:
  %   - degree (polynomials of this degree will be integrated exactly)
  %
  % Created: Antti Stenvall (antti@stenvall.fi)
  %
  
  degree = max(degree,1);
    
  pointsRequired = ceil((degree+1)/2);  
  switch pointsRequired
    case 1
      W = 1;      
      X = [1,1]/2;      
    case 2
      W = [1;1]/2;      
      X = ([-0.5773502691896257;
        0.5773502691896257]+1)/2;
      X = [X 1-X];
    case 3
      W = [0.8888888888888888;
        0.5555555555555556;
        0.5555555555555556]/2;      
      X = ([0;
        -0.7745966692414834;
        0.7745966692414834]+1)/2;
      X = [X 1-X];
    case 4
      W = [0.6521451548625461;
        0.6521451548625461;
        0.3478548451374538;
        0.3478548451374538]/2;
      X = ([-0.3399810435848563;
        0.3399810435848563;
        -0.8611363115940526;
        0.8611363115940526]+1)/2;
    case 5
      W = [0.5688888888888889;
        0.4786286704993665;
        0.4786286704993665;
        0.2369268850561891;
        0.2369268850561891]/2;
      X = ([0;
        -0.5384693101056831;
        0.5384693101056831;
        -0.9061798459386640;
        0.9061798459386640]+1)/2;
    case 6
      W = [0.3607615730481386;
        0.3607615730481386;
        0.4679139345726910;
        0.4679139345726910;
        0.1713244923791704;
        0.1713244923791704]/2;
      X = ([0.6612093864662645;
        -0.6612093864662645;
        -0.2386191860831969;
        0.2386191860831969;
        -0.9324695142031521;
        0.9324695142031521]+1)/2;
    case 7
      W = [0.4179591836734694;
        0.3818300505051189;
        0.3818300505051189;
        0.2797053914892766;
        0.2797053914892766;
        0.1294849661688697;
        0.1294849661688697]/2;
      X = ([0;
        0.4058451513773972;
        -0.4058451513773972;
        -0.7415311855993945;
        0.7415311855993945;
        -0.9491079123427585;
        0.9491079123427585]+1)/2;    
    case 8
      W = [0.3626837833783620;
        0.3626837833783620;
        0.3137066458778873;
        0.3137066458778873;
        0.2223810344533745;
        0.2223810344533745;
        0.1012285362903763;
        0.1012285362903763]/2;
      X = ([-0.1834346424956498;
        0.1834346424956498;
        -0.5255324099163290;
        0.5255324099163290;
        -0.7966664774136267;
        0.7966664774136267;
        -0.9602898564975363;
        0.9602898564975363]+1)/2;
    otherwise
      error('Unknown degree for Gaussian quadrature.')
  end